export function generateSessionName(rng: () => number = Math.random): string {
  const w1 = WORDS[Math.floor(rng() * WORDS.length)] ?? WORDS[0];
  const w2 = WORDS[Math.floor(rng() * WORDS.length)] ?? WORDS[0];
  const n = Math.floor(rng() * 1000)
    .toString()
    .padStart(3, "0");
  return `${w1}-${w2}-${n}`;
}
